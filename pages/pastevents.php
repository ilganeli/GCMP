		  <a class="fancybox" href="#inline3" title="Program Notes">PROGRAM NOTES</a><br />
			by Dr. Yona Stamatis</p><br />

		  <div id="inline3" style="width:550px;display: none;" class="popupbackground">
			<p><span style="color:#9dcff6; font-size:25px;"><strong>It Sounds Greek to Me, But Why?</strong></span><br />
			<span style="font-size:20px;"><em>Program notes by Dr. Yona Stamatis, who presented a pre-concert talk for Only on Sunday II </em></span></p>

			<p>What is Greek music? Are certain genres of music more Greek than others? Only on Sunday II is an accessible introduction to the growing diversity of contemporary Greek 	
			music. The program features a wide range of musical genres including jazz, Western art music, folk song and popular song. Its composers and performers insert recognizably 	
			Greek elements into these diverse genres and raise questions about musical meaning and about Greek identity. For example, does the Greek ancestry of the composers and 
			performers define the national identity of their works? If so, then the categorization “Greek music” becomes so broad that it is arguably useless. If not, what criteria 
			exist to determine the national identity of a piece of music? </p>

			<p>Each composition on the program challenges dominant ideologies about Greek music in a different way. The two songs “Grand Sousta” and “Kalamatianos” from the Sonata “For 
			a Little White Seashell” by Manos Hadjidakis feature folk melodies and rhythms transcribed for piano. Though Hadjidakis decorates the music with unexpected harmonies and 
			splashes of color, his short phrases and driving dance rhythms keep the music accessible and recognizably Greek. The Violin Sonata No. 1 by Dimitris Dragatakis also features 		Greek folk idioms including the polyphonic mirologi (funeral lament) from his hometown in Epirus. Yet Dragatakis’s use of serial compositional technique leaves the listener 	
			struggling to decipher the folk music elements. His composition is held together not by memorable melodies but by short repeating rhythmic motifs that pass back and forth 	
			between the violin and the piano. In his composition Homage – A Folk Concerto for Flute and Orchestra, Dinos Constantinides references Greekness in another way: He creates 	
			melodies that resemble Greek folk music but that do not reference actual folk material. This is especially evident in the third movement of the concerto, an energetic 	
			folk-like dance with a driving rhythmic pattern.</p>

			<p>The central portion of the program features a selection of common folk and popular songs from Greece including “My Little Painted Boat” and “The Fishing Net.” Yet, in 	
			their performance of these songs, singer Mariangela Chatzistamatiou and pianist Pantelis Polychronidis challenge the listener’s understanding of Greek music. They interpret 	
			the songs in a new way that gives primacy to the text rather than to the music, even stretching the music in unconventional ways in service to the text. </p>

			<p>The concert closes with a new composition titled “Ousak,” by University of Michigan graduate Michael Malis. In this work, Malis intentionally tests the boundaries of 	
			musical style and musical identity. In his own words, “the piece is not traditional music and it is not jazz either. It definitely is not Greek jazz. I detest the term.” 
			This work for saxophone and piano features an original setting of the traditional ousak mode, a scale pattern that permeates much of traditional Greek music. Yet Malis 	
			approaches the ousak mode at an angle, altering the common trajectory of the mode with unconventional harmonization and jazz improvisation. </p>

			<p>Is this a concert of Greek music? The question is open to debate. As you listen to this concert, formulate your own opinion about the Greekness of the music. I guarantee 	
			a richer concert experience. </p>
			
			<hr width="200" size="1" color="#999999" align="center" />

			</p><strong>Dr. Yona Stamatis</strong> is a professor at the University of Illinois in Sprinfield, where she teaches American and world music. Her research centers on 	
			traditional Greek song with a specific focus on rebetiko performance in the context of the contemporary Greek crisis. As a Fulbright scholar and again as a Tsangadas Fellow, 		she researched rebetika in Athens, Greece where she also performed violin and bouzouki with famed rebetiko band Rebetiki Istoria. In 2011, she successfully defended her 	
			dissertation &quot;Rebetiko Nation: Hearing Pavlos Vassiliou’s Alternative Greekness in Rebetiko Song.&quot;</p>

