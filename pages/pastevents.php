    <a name="prometheas" />
  	<p><span style="color:#9dcff6; font-size:18px;">Manos Hadjidakis: A New Generation of Musicians Pays Tribute to the Composer</span><br />
    <span style="font-size:14px;"><em>Saturday, April 18, 2015</em></span><br />
    Kerrytown Concert House<br />
	Ann Arbor, Michigan, U.S.A.</p>
	
	<p><img src="hadjidakis-pics.jpg" alt="Young musicians pay tribute to Manos Hadjidakis with unique arrangements of his music by Michael Malis" title="Young musicians pay tribute to Manos Hadjidakis with unique arrangements of his music by Michael Malis" /></p>
	
	<p>Composer, pianist, and improvising musician <strong>Michael Malis</strong> spearheaded this concert in a program of new adaptations of some of Manos Hadjidakis’ best-known compositions, as well as some of his lesser-known gems. Malis, who is of Greek-American descent, brought a unique perspective to this music, seamlessly blending jazz, classical, folk, and creative music. The sold-out concert featured solo piano, as well as newly formed quartet comprised of  <strong>Michele Ramo</strong> (mandolin and fretless guitar); flutist and GCMP founder <strong>Ellie Falaris Ganelin</strong>; and cellist <strong>Abigail Alwin</strong>, an improvising musician with a deep affinity for Greek music.</p>
    <p>Also featured on the program was a lively performance of Ann Arbor’s own <strong><a href="https://ornamatik.squarespace.com/" target="_blank" class="link">Ornâmatik</a></strong>, a “Balkan funk band” featuring popular area musicians. Well-known for their legendary festive performances around Ann Arbor, Southeastern Michigan, and across the Midwest, OrnämatiK consistently delights dancers and audiences alike.</p>
    <p><em>This concert was made possible with the support of the Modern Greek Program at The University of Michigan and in part by an award from the Michigan Council for Arts and Cultural Affairs.</em></p>
