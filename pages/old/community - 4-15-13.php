<div class="grid_7 prefix_1">
    <div id="openingtext">
  	<p><em>Much of what is celebrated in Greek culture today is the poetry, music, drama, philosophy and so on of years past. The Greek Chamber Music Project strives to promote the Greek culture of the now and to cultivate that of the future. </em></p>
    
	
	<p><em><strong>Our goal:</strong> to create a central hub for listings of Greek musical performances, CD releases and other musical news around the world. Help us to build the calendar and spread the word about this rich and exciting music.</em>
	</p>
    </div>

    

    <p>Are you a performer, presenter or academic institution that will be featuring music by Greek composers on an upcoming program? We would like to highlight any public performances of classical or jazz music by composers who are Greek or of Greek descent. An upcoming CD release, lecture, presentation, festival, etc. are also acceptable. </p>
<p><strong>Submission guidelines:</strong> please include a short description of the performance; title of the work(s) being performed; the composer(s); names of the artists and their instruments/voice parts; and the date, time and  address of the performance. Submissions may include a single piece by a Greek composer on a larger program. <em>Works in classical, jazz and similar genres are preferred.</em></p>
      <p>To have a performance considered for the community calendar, please send your submission to: <br />
      <a href="mailto:greekchambermusic@gmail.com" target="_blank">greekchambermusic@gmail.com</a>.</p>
</div>
