    Only on Sunday is an annual concert series that takes place at  <a href="http://www.kerrytownconcerthouse.com" target="_blank" class="link">Kerrytown Concert House</a> in Ann Arbor, Michigan is are co-sponsored by the <a href="http://www.lsa.umich.edu/modgreek" target="_blank" class="link">Greek Department at the University of Michigan</a>. Additional support has been provided by the Michigan Council for the Arts and Cultural Affairs and the National Endowment for the Arts. The model for these concerts is to alternate between Western classical works and familiar <em>laïká</em> and folk songs, evoking both nostalgia for the familiar and excitement for the new. But no work purely fits into a particular style &mdash;
the popular songs are lyrical with a stylized, art song feel, while the classical selections often have Greek influences like scales, modes and dance rhythms. <br />
