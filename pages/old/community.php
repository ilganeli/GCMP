    <p>Are you a performer, presenter or academic institution that will be featuring music by Greek composers on an upcoming program? We would like to highlight any public performances of classical or jazz music by composers who are Greek or of Greek descent. </p>
<p><strong>Submission guidelines:</strong> please include a short description of the performance; title of the work(s) being performed; the composer(s); names of the artists and their instruments/voice parts; and the date, time and  address of the performance. Submissions may include a single piece by a Greek composer on a larger program. <br />
  Works in classical, jazz and similar genres are preferred.</p>
      <p>To have a performance considered for the community calendar, please send your submission to: <br />
      <a href="mailto:greekchambermusic@gmail.com" target="_blank">greekchambermusic@gmail.com</a>.</p>
