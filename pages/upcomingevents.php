	<span style="color:#3abdfe; font-size:16px;">New Record Label</span><br />
	<p>The Greek Chamber Music Project is pleased to announce the launch of our very own record label. The goal is to spread the music of Greek composers around the world and to support the young artists who make this wonderful music. Our debut CD, titled <strong>Hellenic Song: A Musical Migration</strong>, is a collection of sonatas, suites, and popular songs by contemporary Greek composers featuring Mary-Victoria Voutsas (piano), Anna Matijasic Hennessy (violin), and Ellie Falaris Ganelin (flute). Join us in making the production of this CD and the new label a reality by <a href="https://www.indiegogo.com/projects/greek-chamber-music-project-label-cd-launch/x/10538062" class="link" target="_blank">pre-ordering our debut CD</a>!</p>
